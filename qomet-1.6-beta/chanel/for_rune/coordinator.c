uint8_t *
hex2bin(char *hex, uint8_t *bin, size_t len)
{
	int i;
	uint8_t val;

	for(i = 0; i < len; i++) {
		if (('0' <= *(hex + i * 2)) && (*(hex + i * 2) <= '9'))
			val = (*(hex + i * 2) - '0');
		else if (('a' <= *(hex + i * 2)) && (*(hex + i * 2) <= 'f'))
			val = (*(hex + i * 2) - 'a' + 10);
		else if (('A' <= *(hex + i * 2)) && (*(hex + i * 2) <= 'F'))
			val = (*(hex + i * 2) - 'A' + 10);
		else
			return NULL;
		val *= 16;
		if (('0' <= *(hex + i * 2 + 1)) && (*(hex + i * 2 + 1) <= '9'))
			val += (*(hex + i * 2 + 1) - '0');
		else if (('a' <= *(hex + i * 2 + 1))
		  && (*(hex + i * 2 + 1) <= 'f'))
			val += (*(hex + i * 2 + 1) - 'a' + 10);
		else if (('A' <= *(hex + i * 2 + 1))
		  && (*(hex + i * 2 + 1) <= 'F'))
			val += (*(hex + i * 2 + 1) - 'A' + 10);
		else
			return NULL;
		bin[i] = val;
	}
	return bin;
}

void *
coordinator_init(int gsid)
{
	int len;
	uint8_t *bin;
	jlecommonstruct *p;

	if((p = (jlecommonstruct *)malloc(sizeof(*p))) == NULL)
		return NULL;
	p->gsid = gsid;
	p->plen = 0;
	if(pthread_mutex_init(&p->mutex, NULL) != 0) {
		RUNE_INFO("failed to initialize mutex.\n");
		return NULL;
	}
	len = strlen(coordinatorfw) / 2;
	if((bin = (uint8_t *)malloc(len)) == NULL) {
		RUNE_INFO("failed to allocate memory.\n");
		return NULL;
	}
	strlcpy(p->path, "/tmp/coordinator.XXXXXXXX", sizeof(p->path));
	if((p->fd = mkstemp(p->path)) == -1) {
		RUNE_INFO("failed to create temporary file.\n");
		return NULL;
	}
	if(hex2bin(coordinatorfw, bin, len) == NULL) {
		RUNE_INFO("invalid firmware image.\n");
		return NULL;
	}
	write(p->fd, bin, len);
	return p;
}

int
coordinator_step(void *p)
{
	jlecommonstruct *s = getStorage(p);

	if(ore_init(s->path, 1, "TS", "ON", "OFF", 1, s->hw, s->st, p)
	  != 0) {
		unlink(s->path);
		RUNE_INFO("failed to initiate ore instance.\n");
		return -1;
	}
	unlink(s->path);
	ore_cmd_start(0);
	sleep(180);
	return -1; 
}

void
coordinator_fin(void *p)
{
	jlecommonstruct *s = getStorage(p);

	close(s->fd);
	free(s);
}

