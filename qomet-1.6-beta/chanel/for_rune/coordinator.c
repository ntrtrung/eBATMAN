void *
coordinator_write(void *p, void *a)
{
	localSpaceList *l = p;
	condPacket *packet = (condPacket *)a;
	jleCondData *data = (jleCondData *)&packet->data;
	jlecommonstruct *s = getStorage(p);

	RUNE_INFO("%s received write request from space %d including "
	  "%d bytes data\n", l->name, ntohl(packet->sgsid),
	  ntohl(data->len));
	pthread_mutex_lock(&s->mutex);
	s->plen = ntohl(data->len);
	bcopy(data->data, s->buff, s->plen);
	pthread_mutex_unlock(&s->mutex);
	s->response.num = 0;
	s->response.packet.data.len = htonl(sizeof(int));
	return &s->response;
}

